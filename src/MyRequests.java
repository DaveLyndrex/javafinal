    private void cancelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cancelMouseClicked
        DefaultTableModel model = (DefaultTableModel) myRequestsTable.getModel();
        int row = myRequestsTable.getSelectedRow();

        try {

            JFrame frame = new JFrame("Cancel");
            if (JOptionPane.showConfirmDialog(frame, "Are you sure you want to cance;?", "Super Admin Message", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION) {
                int identify = Integer.parseInt(model.getValueAt(row, 0).toString());
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/javafinal", "root", "");
                PreparedStatement stmt = con.prepareStatement("DELETE FROM requestproducts WHERE product_id =?");
                stmt.setInt(1, identify);
                stmt.executeUpdate();
                model.removeRow(row);

                JOptionPane.showMessageDialog(null, "Cancel Successfully!", "Message from MySQL", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Message from MySQL", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_cancelMouseClicked

