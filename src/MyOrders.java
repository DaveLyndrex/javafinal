    public void getData(){
        int count = 0;
        //selecting all in ordered products
        try {
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/javafinal", "root", "");
           Statement stmt = con.createStatement();

           ResultSet data = stmt.executeQuery("SELECT * FROM `orderedproducts`");
           DefaultTableModel model = (DefaultTableModel) myOrdersTable.getModel();
           while (data.next()) {
              
               count = 1;
               model.addRow(new Object[]{data.getInt("order_id"), data.getString("photo"), data.getString("name"), data.getString("brand"), data.getString("description"), data.getInt("totalPrice"),data.getInt("quantity"), data.getString("status")});

           }

           if (count == 0) {
               JOptionPane.showMessageDialog(null, "No data found!.", "Message from MySQL", JOptionPane.WARNING_MESSAGE);
           }
           con.close();
       } catch (Exception e) {
           System.out.println(e.getMessage());
       }
    }

