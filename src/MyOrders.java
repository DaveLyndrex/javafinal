    private void cancelBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cancelBtnMouseClicked
            DefaultTableModel model = (DefaultTableModel) myOrdersTable.getModel();
        int row = myOrdersTable.getSelectedRow();
       
        try{
           
           JFrame frame = new JFrame ("Cancel Orders");
           if (JOptionPane.showConfirmDialog(frame, "Are you sure you want to cancel this order?"," Super Admin Message", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION){
               int identify = Integer.parseInt(model.getValueAt(row, 0).toString());
               Class.forName("com.mysql.jdbc.Driver");
               Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/javafinal","root","");
               PreparedStatement stmt = con.prepareStatement("DELETE FROM orderedproducts WHERE product_id =?");
               stmt.setInt(1,identify);
               stmt.executeUpdate();
               model.removeRow(row);
              
               JOptionPane.showMessageDialog(null, "Successfully Cancelled.", "Message from MySQL", JOptionPane.INFORMATION_MESSAGE);
           }
       }catch (Exception e){
           JOptionPane.showMessageDialog(null, e.getMessage(),"Message from MySQL", JOptionPane.WARNING_MESSAGE);
       }

    }//GEN-LAST:event_cancelBtnMouseClicked

