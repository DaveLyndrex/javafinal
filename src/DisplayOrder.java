    private void approveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_approveBtnMouseClicked
        String orderId = order_id.getText();
        String productId = product_id.getText();
        String productPhoto = photo.getText();
        String productName = name.getText();
        String productBrand = brand.getText();
        String productDescription = description.getText();
        int productQuantity = Integer.parseInt(quantity.getText());
        int productTotalPrice = Integer.parseInt(totalPrice.getText());
       
       
        
        
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/javafinal", "root", "");
            Statement stmt = (Statement) con.createStatement();
            String query = "INSERT INTO `approveorders` (`order_id`,`photo`,`name`,`brand`,`description`, `totalprice`, `quantity`) VALUES (NULL, '"+productPhoto+"', '"+productName+"', '"+productBrand+"', '"+productDescription+"', '"+productTotalPrice+"', '"+productQuantity+"')";
            stmt.executeUpdate(query);
            String query2 = "DELETE FROM orderedproducts WHERE order_id = '" + orderId + "';";
            stmt.executeUpdate(query2);
            
            JOptionPane.showMessageDialog(this,"Approved Successfully.");
            
            String queryUpdateQuantity = "UPDATE `products` SET stocks = stocks - '" + productQuantity + "' WHERE product_id='" + productId + "';";
            stmt.executeUpdate(queryUpdateQuantity);
            
            new ApprovedOrders().setVisible(true);
            this.setVisible(false);
           
            JOptionPane.showMessageDialog(null, "New approved product added.", "Message from MySQL", JOptionPane.INFORMATION_MESSAGE);
            con.close();
        }catch (Exception e){
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_approveBtnMouseClicked

